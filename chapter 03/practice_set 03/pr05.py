# Most Programming languages use a backslash \ as an escape character. \f: This is called form feed, which is used to indicate to a printer that it should start a new page.

#  A carriage return is a simple escape character that operates similarly to \n. However, unlike \n, which moves the cursor to a new line, \r shifts the cursor to the beginning of the current line